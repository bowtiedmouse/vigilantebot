# Discord options:
#
# > !vigilante target <address> <alias>: adds new target to watch list
# > !vigilante watch <address or alias>: only updates for that target
# > !vigilante [watch]: updates for all targets (default option)
# > !vigilante tagme <address or alias> <token>: @tags user when target has activity
#   on a token
# > !vigilante tagme <token>: @tags user when there's activity on a token by any target
